 <h1>Kegiatan UKM Kristen </h1>

<hr>
<h2>Ibadah Rutin</h2>
  <p> Sebagai penyegaran rohani. Setiap hari kita diperhadapkan dengan banyak pergumulan dan masalah, karena itu kita membutuhkan kekuatan dan penghiburan.  Dan ibadah menjadi jawaban atas itu semua.  Melalui firman Tuhan kita diingatkan kembali betapa kita memiliki Tuhan yang heran dan ajaib.  Masalah yang kita alami tidak sebanding dengan kebesaran dan kuasa Tuhan.  Hal ini memberikan ketenangan hidup bagi kita. </p>

 <h2> Ibdah Bersama Armed</h2>
  <p>Pelayanan. Dalam ibadah kita berkumpul dengan keluarga besar Kerajaan Allah. Ada tertulis: "Demikianlah kamu bukan lagi orang asing dan pendatang, melainkan kawan sewarga dari orang-orang kudus dan anggota-anggota keluarga Allah."  (Efesus 2:19).  Oleh karena itu kita harus saling mengasihi dan melengkapi satu sama lain. Dan dalam ibadah inilah kita memiliki kesempatan untuk melayani Tuhan karena melayani itu bukanlah hak istimewa para hamba Tuhan atau profesional di bidang rohani saja;  setiap orang Kristen adalah hamba yang melayani  (baca1 Petrus 2:9).</p>


  <h2> Ibdah Natal & Tahun Baru </h2>
  <p>Kelahiran Kristus di dunia mempunyai suatu titik awal yang paling penting dalam misi Kristus. Dilahirkan bukan dari pencampuran laki-laki dan perempuan, melainkan secara campur tangan Allah yakni diperanakan oleh kuasa Roh Allah (Mat 1:18,20). Maria, seorang gadis saleh, mendapat kehormatan sebagai perantara kedatangan Sang Mesias (Luk 1:26-33). Adapun Kristus datang untuk memperbaiki hubungan manusia dengan Allah yang semakin buruk oleh karena kesesatan manusia.</p>
