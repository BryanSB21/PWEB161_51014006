<h1>Teknologi Perbankan</h1>
<hr>

<p>Teknologi perbankan saat ini telah mencapai pada level yang dikatakan maju dibandingkan beberapa tahun kebelakang. Produk-produk yang dihasilkan cukup banyak, saya akan menjelaskan beberapa produk bank yang saya dapat dari pelajaran Terapan Komputer Perbankan.</p>

<p>Pertama yaitu ATM atau yang dikenal dengan Anjungan Tunai Mandiri. Ini merupakan produk bank yang sudah sangat kita kenal atau jumpai hampir disetiap tempat dan bahkan merupakan mesin kasir otomatis pengganti orang karena kita tidak perlu lagi harus mengantri panjang untuk hanya melakukan pembayaran, penarikan atau penyetoran dengan jumlah yang tidak terlalu banyak.</p>

<p>Menurut perbankan berdasarkan jenisnya ATM berfungsi sebagai berikut :</p>
<ul>
	<li>Multifungsi</li>
	<li>Tarik Tunai</li>
	<li>Non Tunai</li>
	<li>Setoran Tunai</li>
</ul>


<p>Pengoperasian ATM dapat dilakukan melalui dua cara yaitu Onpromise yaitu terhubung dengan server dimana ATM tersebut ditempatkan. Dan Offpromise yaitu pengoperasian ATM yang menggunakan satelit khusus.</p>

<p>Strategi pengelolaan yang dapat dilakukan ATM yaitu dilakukan sendiri atau bersama. Kalau contoh dikelola sendiri itu in house dan out sourcing. Kalau in house pasti tidak sulit kita temui. Contohnya itu bila kita melihat dimesin ATM hanya terdapat satu nama bank saja maka itu disebut in house. Sedangkan bila kita melihat banyak nama bank pada satu mesin ATM maka itu dapat dikatakan out sourcing. Sedangkan bila stategi pengelolaan yang dikatakan bersama itu seperti joint ventura dan pihak ke-3. Kedianya memiliki arti yang hamper sama yaitu bank bergabung dengan pihak lain selain bank mereka sendiri dan membayarkan sewa pada pihak tersebut.</p>

<p>Yang kedua dari teknologi perbankan yaitu Kartu Plastik. ATM tidak akan berfungsi bila tanpa kartu plastik ini. Kartu plastic memiliki banyak jenisnya diantaranya :</p>

<ul>
	<li>Credit card</li>
	<li>Smart card</li>
	<li>Debit card</li>
	<li>Private label card</li>
	<li>Change card</li>
</ul>


<p>Dimulai dari credit card, pastilah tidak asing kita mendengar kata-kata ini apa lagi belakangan ini banyak kamsus yang berkaitan dengan kartu ini. Bank lebih berminat menarik nasabah melalui kartu ini hal tersebut dikarenakan bunga yang dibebankan lebih besar dari pada bunga dari tabungan. Sehingga kita perlu lebih berhati-hati bila menggunakan kartu ini.</p>

<p>Smart cart, pasti kurang fasi kita dengar tapi bila disebut dengan kartu flash pasti banyak yang mengerti. Kartu flash keluaran BCA itu merupakan contoh dari smart card tersebut. Yang membedakan kartu ini dengan kartu yang lain yaitu kartu ini kita isikan saldo terlebih dahulu kemudian baru dapat kita gunakan dan tidak ada potongan ataupun bunga yang dibebankan kepada kita. Serta tidak terhubung dengan rekening kita.</p>

<p>Kartu yang kita miliki dikatakan debit card. Atau kartu yang kita lakukan untuk penarikan tunai, pembayaran, atau pengiriman uang. Semua itu merupakan kegunaan dari kartu ini. Selain itu pada kartu ini bunga yang didapat lebih kecil dibandingkan dengan credit card. Pada proses atau transaksi yang dilakukan dengan menggubnakan kartu ini terhubung dengan rekening yang kita miliki.</p>

<p>Change card pasti belakangan ini sangat jarang kita dengar. Tetapi bank mengeluarkan kartu ini. Sama halnya dengan credit card yaitu melakukan pinjaman dan tidak terhubung ke rekening kita. Tetapi yang membedakan yaitu cara pelunasannya. Bila credit card dapat melakuakn pelunasan secara bertahap atau setiap bulan tetapi lain halnya dengan kartu ini. Kartu ini membebankan nasabah dengan melunasi seluruh pinjaman yang dilakukan sesuai dengan jatuh tempo yang telah disepakati.</p>
